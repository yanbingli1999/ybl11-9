export function getRandomRelic(depth: number): Relic {
  const genuineChance = Math.max(0.3, 0.7 - depth * 0.05);
  const isGenuine = Math.random() < genuineChance;

  const genuineRelics = RELICS.filter((r) => r.isGenuine);
  const fakeRelics = RELICS.filter((r) => !r.isGenuine);

  const pool = isGenuine ? genuineRelics : fakeRelics;
  return pool[Math.floor(Math.random() * pool.length)];
}
